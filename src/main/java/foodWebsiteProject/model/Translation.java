package foodWebsiteProject.model;

public class Translation {
    private String name;
    private Integer id;

    public Translation() {}

    public String getName() {
        return name;
    }
    public Integer getId() {
        return id;
    }

    public void setName(String name) {
        this.name = name;
    }
    public void setId(Integer id) {
        this.id = id;
    }
}
